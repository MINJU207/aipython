# 아스카 코드 그림 출력 하기

# 만약에 1을 입력하면 1번 캐릭터 출력
# 만약에 2를 입력하면 2번 캐릭터 출력
# 3을 입력하면 3번 캐릭터 출력
# 잘못입력하면 잘못 입력했다고 출력

# 고양이 캐릭터 아스카 코드 그림 출력하기

# 고양이 캐릭터 예시 (1, 2, 3번에 해당하는 캐릭터)
cats = {
    1: r"""
     /\_/\  
    ( o.o ) 
     > ^ <  """,
    
    2: r"""
     /\_/\  
    ( =^.^= ) 
     (")(")  """,
    
    3: r"""
     /\_/\  
    ( -.- ) 
     > ^ <  """,
}

# 사용자 입력 받기
try:
    user_input = int(input("1, 2, 3 중 하나를 입력하세요: "))
    
    # 잘못된 입력 처리
    if user_input in cats:
        print(cats[user_input])
    else:
        print("잘못 입력했습니다.")
except ValueError:
    print("잘못 입력했습니다. 숫자를 입력해주세요.")

